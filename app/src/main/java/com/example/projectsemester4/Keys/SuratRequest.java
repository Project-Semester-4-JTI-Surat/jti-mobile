package com.example.projectsemester4.Keys;

public class SuratRequest {
    private String kepada;
    private String alamat;
    private String tanggal;
    private String jenisSurat;
    private String namaDosen;
    private String kebutuhan;
    private String nimAnggota;
    private String namaAnggota;
    private String tlpAnggota;
    private String prodiAnggota;

    public SuratRequest() {
        this.kepada = kepada;
        this.alamat = alamat;
        this.tanggal = tanggal;
        this.jenisSurat = jenisSurat;
        this.namaDosen = namaDosen;
        this.kebutuhan = kebutuhan;
        this.nimAnggota = nimAnggota;
        this.namaAnggota = namaAnggota;
        this.tlpAnggota = tlpAnggota;
        this.prodiAnggota = prodiAnggota;
    }

    public String getKepada() {
        return kepada;
    }

    public void setKepada(String kepada) {
        this.kepada = kepada;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getJenisSurat() {
        return jenisSurat;
    }

    public void setJenisSurat(String jenisSurat) {
        this.jenisSurat = jenisSurat;
    }

    public String getNamaDosen() {
        return namaDosen;
    }

    public void setNamaDosen(String namaDosen) {
        this.namaDosen = namaDosen;
    }

    public String getKebutuhan() {
        return kebutuhan;
    }

    public void setKebutuhan(String kebutuhan) {
        this.kebutuhan = kebutuhan;
    }

    public String getNimAnggota() {
        return nimAnggota;
    }

    public void setNimAnggota(String nimAnggota) {
        this.nimAnggota = nimAnggota;
    }

    public String getNamaAnggota() {
        return namaAnggota;
    }

    public void setNamaAnggota(String namaAnggota) {
        this.namaAnggota = namaAnggota;
    }

    public String getTlpAnggota() {
        return tlpAnggota;
    }

    public void setTlpAnggota(String tlpAnggota) {
        this.tlpAnggota = tlpAnggota;
    }

    public String getProdiAnggota() {
        return prodiAnggota;
    }

    public void setProdiAnggota(String prodiAnggota) {
        this.prodiAnggota = prodiAnggota;
    }
}
